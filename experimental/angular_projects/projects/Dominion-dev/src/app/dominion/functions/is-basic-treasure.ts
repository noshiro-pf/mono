export const isBasicTreasure = (cardId: string): boolean =>
       cardId === 'Copper'
    || cardId === 'Silver'
    || cardId === 'Gold'
    || cardId === 'Platinum'
    || cardId === 'Potion'
    || cardId === 'Harem'
    || cardId === 'Stash'
    || cardId === 'Humble_Castle'
    || cardId === 'Pasture'
    || cardId === 'Pouch'
  ;
